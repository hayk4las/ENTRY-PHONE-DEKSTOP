    private void buttonTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonTambahActionPerformed
        // TODO add your handling code here:
        barang.setImei(textImei.getText());
        barang.setMerk_hp(textMerkHp.getText());
        barang.setStorage(textStorage.getText());

        try {
            barang.setHarga(Integer.valueOf(String.valueOf(textHarga.getValue())));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Harga tidak valid", "KESALAHAN", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            // Simpan data ke database
            connection.setAutoCommit(false);
            barangModel.insert(barang);
            connection.commit();

            // Tampilkan pesan berhasil
            JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan", "INFORMASI", JOptionPane.INFORMATION_MESSAGE);

            // Reset form untuk entri baru
            buttonBaruActionPerformed(evt);
        } catch (SQLException ex) {
            // Rollback jika terjadi kesalahan
            try {
                connection.rollback();
            } catch (SQLException rollbackEx) {
                rollbackEx.printStackTrace();
            }
            JOptionPane.showMessageDialog(this, "Data gagal ditambahkan: " + ex.getMessage(), "KESALAHAN", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_buttonTambahActionPerformed


    private void buttonUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonUbahActionPerformed
     
    // Mengambil data dari form
    barang.setImei(textImei.getText());
    barang.setMerk_hp(textMerkHp.getText());
    barang.setStorage(textStorage.getText());

    // Ambil harga satuan dari form
    try {
        barang.setHarga(Integer.valueOf(String.valueOf(textHarga.getValue())));
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Harga valid", "KESALAHAN", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Pastikan kode barang tidak kosong
    if (barang.getImei().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Kode barang tidak boleh kosong", "KESALAHAN", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
        // Set auto commit ke false
        connection.setAutoCommit(false);

        // Update data barang di database
        barangModel.update(barang);

        // Commit perubahan
        connection.commit();

        // Tampilkan pesan berhasil
        JOptionPane.showMessageDialog(this, "Data barang berhasil diubah", "INFORMASI", JOptionPane.INFORMATION_MESSAGE);
    } catch (SQLException ex) {
        try {
            // Rollback jika terjadi kesalahan
            connection.rollback();
        } catch (SQLException rollbackEx) {
            rollbackEx.printStackTrace();
        }

        // Tampilkan pesan gagal
        JOptionPane.showMessageDialog(this, "Data barang gagal diubah: " + ex.getMessage(), "KESALAHAN", JOptionPane.ERROR_MESSAGE);
        ex.printStackTrace();
    }

    }//GEN-LAST:event_buttonUbahActionPerformed


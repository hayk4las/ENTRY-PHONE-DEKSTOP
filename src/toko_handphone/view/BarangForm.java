    private void buttonHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonHapusActionPerformed

    // TODO add your handling code here:
        int confirm = JOptionPane.showConfirmDialog(this, 
        "Apakah Anda yakin ingin menghapus barang dengan kode " + textImei.getText() + "?",
        "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);

    // Jika pengguna memilih "Yes"
    if (confirm == JOptionPane.YES_OPTION) {
        try {
            // Set kode barang yang akan dihapus
            barang.setImei(textImei.getText());

            // Hapus barang dari database
            connection.setAutoCommit(false);
            barangModel.delete(barang);
            connection.commit();

            // Tampilkan pesan berhasil
            JOptionPane.showMessageDialog(this, "Barang berhasil dihapus", "INFORMASI", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException ex) {
            // Tampilkan pesan gagal
            JOptionPane.showMessageDialog(this, "Barang gagal dihapus", "KESALAHAN", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }
    
    }//GEN-LAST:event_buttonHapusActionPerformed

